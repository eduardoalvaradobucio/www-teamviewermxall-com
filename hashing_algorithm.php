<p><em>The Redvers Hashing Algorithm creates standard NIST SHA message digests so that the generated hash values will match values generated by other standard SHA hashing algorithms.</em></p>

<h3>Technical Information</h3>

<p>The <strong>Redvers Hashing Algorithm</strong> 2.4 supports thirteen hash functions within the three SHA (Secure Hash Algorithm) family types:</p>    
<ul>
  <li>SHA-1 (160 bits)</li>
  <li>SHA-2 (224, 256, 384 and 512 bits) plus two truncated 512 functions (SHA-512/224 and SHA-512/256)</li>
  <li>SHA-3 (224, 256, 384 and 512 bits) plus two Extendable-Output functions (SHAKE128 and SHAKE256)</li>
</ul>

<p>Specifications for the SHA-1 and SHA-2 algorithms can be found in <a href="https://www.nist.gov/" title="National Institute of Standards and Technology" class="externalText" target="_blank">NIST</a> (National Institute of Standards and Technology) <a href="https://csrc.nist.gov/publications/detail/fips/180/4/final" title="FIPS PUB 180-4" class="externalText" target="_blank">FIPS Publication 180-4</a>. Specification for the SHA-3 algorithm can be found in NIST <a href="https://csrc.nist.gov/publications/detail/fips/202/final" title="FIPS PUB 202" class="externalText" target="_blank">FIPS Publication 202</a>. SHA-3 is based on the Keccak algorithm, defined in the <a href="https://keccak.team/files/Keccak-reference-3.0.pdf" title="Keccak Reference" class="externalText" target="_blank">Keccak Reference</a> document.</p>

<p>The <strong>Redvers Hashing Algorithm</strong> can also be used to generate keyed-hash based Message Authentication Codes (HMACs). Specification for HMAC generation can be found in NIST <a href="https://csrc.nist.gov/publications/detail/fips/198/1/final" title="FIPS PUB 198-1" class="externalText" target="_blank">FIPS Publication 198-1</a>.</p>

<p>Information passed to <strong>RCHASH</strong> can consist of a single data string or a series of strings from an input file or database row. The resulting hash/MAC is returned in binary, hexadecimal and <a href="https://tools.ietf.org/html/rfc4648" title="Internet Engineering Task Force - Request for Comments 4648" class="externalText" target="_blank">Base64</a> formats for easy application processing.</p>

<p>Although SHA hashing can be used to safely represent confidential information, it cannot be used as a substitute for data encryption if the original data string needs to be recovered. This is because data information is destroyed in the hashing process, making it impossible to recover the original data string from a message digest. If decryption is required, an NIST validated encryption/decryption algorithm like the <a href="data_encryption.php" title="COBOL AES Encryption">Redvers Encryption Module</a> is recommended.</p>

