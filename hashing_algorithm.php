<h2>Redvers Hashing Algorithm</h2>

<p><strong>The Redvers Hashing Algorithm can be used to produce SHA-1, SHA-2 or SHA-3 message digests of 224, 256, 384 or 512 bit lengths, ensuring safe, authenticated data transfer to/from any location.</strong></p><br>

<strong>Main features:</strong> 
<ul>
  <li>Runs on any COBOL platform</li>
  <li>Distributed in COBOL source code (&quot;cloaked&quot;)</li>
  <li>Calculates SHA-1, SHA-2 and SHA-3 series hash totals, in standard, truncated or Extendable-Output forms</li>
  <li>Hash MAC (HMAC) generation</li>
  <li>Hash produced in binary, hexadecimal and Base64 formats</li>
  <li>Fast, efficient, professional and fully scalable</li>
  <li>Supports calls from batch or on-line (eg: CICS)</li>
  <li><a href="hashing_algorithm_free_trial.php" title="COBOL Hashing Algorithm - Download a Free Trial">Free 30 day trial</a></li>
</ul>
                                                            
<p>Data selected for hashing or MAC generation can consist of a single data string or a series of multiple strings resulting in a single hash total (message digest) or MAC.</p>

<p>Message digests can be used within many security reliant applications for encryption key derivation, pseudorandom number generation and to generate or verify digital signatures.</p>

<p>The <strong>Redvers Hashing Algorithm</strong> is used by customers all over the world, running on <strong>iSeries/AS400</strong>, <strong>UNIX</strong>, <strong>HP</strong>, <strong>Linux</strong>, <strong>Fujitsu BS2000</strong>, <strong>Micro Focus</strong> and <strong>IBM mainframe</strong> platforms.</p>

<h3>How it Works</h3>
<p>The <strong>Redvers Hashing Algorithm</strong> consists of an easy to use, COBOL subroutine (<strong>RCHASH</strong>) that is called from an application to hash any single data string or series of strings.</p>

<p>Selecting the SHA-1 algorithm will produce a hash total of 160 bits (20 bytes). SHA-2 and SHA-3 algorithms can produce hash totals of 224 bits (28 bytes), 256 bits (32 bytes), 384 bits (48 bytes) or 512 bits (64 bytes). Truncated SHA-2 totals (SHA-512/224 and SHA-512/256) and Extendable-Output SHA-3 totals (SHAKE128 and SHAKE256) can also be generated, as well as Message Authentication Codes (MACs).</p>

<p>The choice of algorithm (SHA-1, SHA-2 or SHA-3) and hash length is decided by setting an 88 level flag in the calling parameters. The subroutine requires no external files and may be called in batch or on-line modes.</p>

