  <h3>How it Works</h3>
  <p>The <strong>Redvers Cloaking Device</strong> (<strong>RCCLOAK</strong>) is a self-contained batch COBOL program that requires no copybooks, includes or objects. The <strong>RCCLOAK</strong> source code has itself been &quot;cloaked&quot; in order to protect authors and clients from code alterations by unqualified personnel.</p>
  <p>In order to &quot;cloak&quot; a program, just pass the original source code to <strong>RCCLOAK</strong> as a data file and it will write a file containing the equivalent &quot;cloaked&quot; COBOL source code. The &quot;cloaked&quot; source can then be compiled, linked and run in place of the original source.</p>

  <p><img src="images/cobol-cloaking-flow.png" alt="Cloaking Device Flowchart" height="520" width="480"></p>

  <h3>Technical Information</h3>
  <p>The <strong>Redvers Cloaking Device</strong> (2.4) consists of a standard batch COBOL program (<strong>RCCLOAK</strong>) that compiles in the same way as any other COBOL program. It has one input file (<strong>RCCOBIN</strong>) which is assigned to the target source and one output file (<strong>RCCOBOUT</strong>) that produces the &quot;cloaked&quot;/obfuscated source. Both input and output files are sequential 80 byte fixed length records.</p>

  <p>The target source can be any variation of COBOL program, from any machine environment. The only requirements are that the program conforms to the standard 80 byte reference format and contains the <code>DATA DIVISION</code> statement.</p>
  
  <p>A sample section of &quot;cloaked&quot; source code can be seen below:</p>
  <p><img src="images/cobol-cloaking-sample.png" alt="Cloaked Code Sample produced by the Redvers Cloaking Device" height="240" width="480"> </p> 

   <p>Once a program has been &quot;cloaked&quot;, it is not possible to reverse engineer the source so that meaningful text and data names are restored.</p>
  <p>&quot;Cloaked&quot; source code remains compliant with all COBOL standards and can be compiled, linked and executed in the normal way. &quot;Cloaked&quot; programs are currently running all over the world, on <strong>iSeries/AS400</strong>, <strong>UNIX</strong>, <strong>HP</strong>, <strong>Linux</strong>, <strong>Fujitsu BS2000</strong>, <strong>Micro Focus</strong> and <strong>IBM mainframe</strong> platforms.</p>
  <p>It is important to note that the &quot;cloaking&quot; process does not attempt to check the syntax or validity of the input source code. Therefore if compile errors exist before &quot;cloaking&quot;, the same errors will exist afterwards.</p>
  <p>Specific lines within the input source code can be preserved from the &quot;cloaking&quot; process if required. This can be useful when source code is outsourced or offered for sale and certain information needs to be passed to the supplier/customer.</p>
  <p>Version 2.3 of the device introduced the facility to encrypt procedure division literals. If these literals are left in obfuscated code without encryption, they can provide important clues about program functionality to an adversary trying to break into the &quot;cloaked&quot; code.</p>

