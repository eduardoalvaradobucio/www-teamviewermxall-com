<h2>About Redvers Consulting</h2>

<p><strong>Redvers Consulting provide niche software products for the integration, modernization and security of COBOL applications. Our clients are primarily large financial institutions in Europe and North America, although we also have customers in many other business and geographical areas.</strong></p>
<p>Our ability to deliver software in COBOL source code form, gives customers reliable, efficient and perfectly integrated solutions to business needs. Source code distribution also means our software will run on all hardware platforms and operating systems: <em>EBCDIC, ASCII, big endian or little endian</em>.</p>
<p>Redvers Consulting have received many business awards over the years, including winning the <strong>Best use of Technology</strong> category in the Thames Gateway Business Awards - see logos below. We are also business partners with <strong>IBM</strong>, <strong>Micro Focus</strong> and <strong>Fujitsu</strong>.</p>
<p>We offer a range of products, services and tools that breathe new life into your COBOL investments:</p>
<ul>
  <li>Our <a href="data_encryption.php" title="Redvers Encryption Module">Redvers Encryption Module</a> will encrypt your COBOL data with AES</li>
  <li>Our <a href="hashing_algorithm.php" title="Redvers Hashing Algorithm">Redvers Hashing Algorithm</a> will ensure the integrity of your data</li>
  <li>Our <a href="cobol_xml_interface.php" title="Redvers COBOL XML Interface">Redvers COBOL XML Interface</a> will send and receive your COBOL data in XML</li>
  <li>Our <a href="cobol_json_interface.php" title="Redvers COBOL JSON Interface">Redvers COBOL JSON Interface</a> will send and receive your COBOL data in JSON</li>
  <li>Our <a href="cobol_signature.php" title="Redvers COBOL Signature">Redvers COBOL Signature</a> software will securely connect your COBOL applications to blockchain and other external messaging systems</li>
  <li>Our <a href="cloaking_device.php" title="Redvers Cloaking Device">Redvers Cloaking Device</a> will secure the intellectual property in your COBOL source code</li>
  <li>Our <a href="services.php" title="Redvers Batch Optimisation">batch optimisation team</a> will improve the efficiency of your COBOL applications</li>
  <li>Our free <a href="https://www.cobol.org.uk" title="Redvers COBOL Tools">COBOL tools</a> can reformat, compress or exclusive OR data, as well as report on aspects of COBOL source code</li>

</ul><br>

