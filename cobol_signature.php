<h2>Redvers COBOL Signature</h2>

<p><strong>The Redvers COBOL Signature software package combines asymmetric encryption (PKI) with digital signature technology in the form of RSA Laboratories PKCS #1, Digital Signature Standard (DSA) and Elliptic Curve Digital Signature Algorithm (ECDSA) standards.</strong></p><br>

<strong>Main features:</strong> 
<ul>
  <li>All code is 100% pure COBOL</li>
  <li>Runs on any COBOL platform</li>
  <li>Supports public/private keys up to 4096 bits, passed in hex or Base64</li>
  <li>Supports RSA, DSA and ECDSA (elliptic curve) signatures</li>
  <li>Includes SHA-1 and SHA-2 224, 256, 384 and 512 bit hashing</li>
  <li>Distributed in COBOL source code</li>
  <li>Efficient, professional and fully scalable</li>
  <li>Supports calls from batch or on-line (eg: CICS)</li>
  <li><a href="cobol_signature_free_trial.php" title="COBOL Data Signature - Download a Free Trial">Free 30 day trial</a></li>
</ul>

<p>The required security level for PKI cryptography and digital signature generation/verification, depends on the length of public/private keys as well as the choice of SHA-1 or SHA-2 hashing algorithms. Key sizes and hash digest lengths are specified by the calling application to ensure the correct security level is maintained.</p>

<p>Digital signatures provide assurance of the sender's identity and confirmation that the data received has not been altered by unauthorized activity.</p>


