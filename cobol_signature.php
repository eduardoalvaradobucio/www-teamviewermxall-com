<h3>Technical Information</h3>

<p>PKI encryption and associated OAEP padding logic complies with algorithms provided in the RSA Laboratories <a href="https://tools.ietf.org/html/rfc8017" title="PKCS #1 v2.2: RSA Cryptography Standard" class="externalText" target="_blank">PKCS #1 v2.2: RSA Cryptography Standard</a>. Specifically, RSAEP/RSADP are used for encryption/decryption and RSA-OAEP with MGF1 is used in padding and mask generation.</p>

<p>RSA signatures are also created and verified as specified in RSA's <a href="https://tools.ietf.org/html/rfc8017" title="PKCS #1 v2.2: RSA Cryptography Standard" class="externalText" target="_blank">PKCS #1 v2.2: RSA Cryptography Standard</a>. Signature logic currently uses the RSAES-PKCS1-v1_5 signature scheme, although RSASSA-PSS can be added on request. These signatures are particularly suited to JSON Web Token (JWT) transactions.</p>

<p>DSA signatures are created and verified as specified in NIST's <a href="https://csrc.nist.gov/publications/detail/fips/186/4/final" title="FIPS PUB 186-4 Digital Signature Standard" class="externalText" target="_blank">FIPS PUB 186-4 Digital Signature Standard</a>. All L/N bit lengths are supported: 1024/160, 2048/224, 2048/256 and 3072/256. DSA is similar to the <a href="https://en.wikipedia.org/wiki/ElGamal_signature_scheme" title="EIGamal signature scheme" class="externalText" target="_blank">EIGamal signature scheme</a>.</p>

<p>ECDSA signatures conform to the ANSI <a href="https://standards.globalspec.com/std/1955141/ANSI%20X9.62" title="ANS X9.62-2005 Public Key Cryptography for the Financial Services Industry" class="externalText" target="_blank">ANS X9.62-2005 Public Key Cryptography for the Financial Services Industry - The Elliptic Curve Digital Signature Algorithm (ECDSA)</a>. All prime field curves are supported. Binary field curves can be added on request.</p>

<p><strong>Redvers COBOL Signature 2.3</strong> programs can be run on EBCDIC or ASCII character encoded platforms, using big or little endian binary formats. Data exchange between subroutines uses a common communication block containing left justified, space filled parameters in hexadecimal or Base64 formats. All subroutine storage areas containing confidential information are initialized before control is returned to the calling application.</p>

<h3>The Software Package</h3>

<p>The <strong>Redvers COBOL Signature</strong> software package consists of:</p>
<ul>
  <li>A sample COBOL calling application program (<strong>RCSSAMP</strong>).</li>
  <li>Four additional application programs to encrypt (<strong>RCSENCR</strong>), decrypt (<strong>RCSDECR</strong>), sign (<strong>RCSSIGN</strong>) and verify (<strong>RCSVERI</strong>).</li>
  <li>Two Redvers Consulting subroutines (<strong>RCSCALC</strong> and <strong>RCSHASH</strong>).</li>
</ul>

<p>All these programs should be copied to the standard source code library and compiled. <strong>RCSSAMP</strong> will need to be compiled and linked last, before starting the trial.</p>

<p>Included within the software is the Redvers calculator subroutine <strong>RCSCALC</strong>. This routine performs the modulated exponential calculations, modular inverse functions, scalar arithmetic and data conversion within the encryption/signature processes. Also included is the Redvers hashing subroutine <strong>RCSHASH</strong>, which runs SHA-1, SHA-224, SHA-256, SHA-384 and SHA-512 algorithms. <strong>RCSCALC</strong> and <strong>RCSHASH</strong> may be used by clients for other technical application requirements without charge.</p>

