<h2>Redvers COBOL XML Interface</h2> 

<p><strong>The Redvers COBOL XML Interface gives COBOL applications fast, efficient access to the world of XML and web services. This off-the-shelf software is written in pure COBOL and simply requires the coding of a COBOL record layout and CALL statement to generate or parse any well-formed XML.</strong></p><br>

<strong>Main features:</strong>
<ul>
  <li>Runs on all COBOL platforms</li>
  <li>Distributed in COBOL source code (&quot;cloaked&quot;)</li>
  <li>Generates and parses multiple dimension arrays (nested <code>OCCURS</code>)</li>
  <li>Easy maintenance</li>
  <li>Fast, efficient, professional and fully scalable</li>
  <li>No training needed for COBOL programmers</li>
  <li>Supports XML attributes, namespaces, SOAP, entities, CDATA, duplicate tag names and mixed content elements</li>
  <li>Free schema to copybook build tool</li>
  <li>Runs in batch or on-line</li>
  <li><a href="cobol_xml_free_trial.php" title="COBOL XML Interface - Download a Free Trial">Free 30 day trial</a></li>
</ul>

<p>The <strong>Redvers COBOL XML Interface</strong> operates at record level, intelligently building all necessary XML element structures when generating and correctly interpreting XML structures when parsing. This approach replaces complex element level application logic with a single <code>CALL</code> statement that passes the next logical record to/from the application.</p>

<p>Our interface is currently used by customers all over the world, running on <strong>iSeries/AS400</strong>, <strong>UNIX</strong>, <strong>HP</strong>, <strong>Linux</strong>, <strong>Fujitsu BS2000</strong>, <strong>Micro Focus</strong> and <strong>IBM mainframe</strong> platforms.</p>

<p>It is frequently used in Service Oriented Architecture (SOA) projects and to integrate XML messages on IBM's MQ Series with core COBOL applications. It's also suitable for generating and parsing SWIFTNet messages and Single Euro Payments Area (SEPA) applications.</p>

