<h3>How it Works</h3>
<p>Before a COBOL application generates or parses XML, a <strong>COBOL Record Definition (CRD)</strong> is coded, consisting of a working storage field definition for each XML element or attribute (the schema to copybook build tool can be used to create this). The CRD is then placed in the standard copybook library and a <code>COPY/INCLUDE</code> statement is added to the application code.</p>

<p>The CRD source code is also stored in the calling application so it can be passed to the interface routines in the <code>CALL</code>. This can either be done by coding the source into working storage or by reading the source from the copybook library at run time.</p>

<p>To <strong>generate</strong> XML, the COBOL application populates the CRD and passes it to the generation subroutine (<strong>RCXMLGEN</strong>) with the CRD source. <strong>RCXMLGEN</strong> then converts the COBOL data to XML elements &amp; attributes and returns the XML document to the application.</p>
<p>To <strong>parse</strong> XML, the application passes the XML and CRD source to the parser subroutine (<strong>RCXMLPAR</strong>) which returns the COBOL data in the CRD fields.</p>
