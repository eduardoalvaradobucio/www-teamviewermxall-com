<h3>Technical Information</h3>
<p>The <strong>Redvers COBOL XML Interface</strong> 3.1 programs are sold in COBOL source code form and are installed by copying to the site source code library and running the standard COBOL compiler. Compilation at client sites enables installation on multiple platforms and it ensures complete compatibility with all other COBOL applications on the platform.</p>
<p>Using source code also allows our customers to make changes to certain values that would otherwise remain fixed. These values are known as <strong>User Maintained Variables (UMV's)</strong> and they can be tuned to reduce storage requirements or to generate XML in specific formats. Full details on all UMV's can be found in the product user guides.</p>
<p>In order to protect both clients and authors from unauthorized code changes, the subroutine source code has been &quot;cloaked&quot; using the <a href="cloaking_device.php" title="Redvers Cloaking Device">Redvers Cloaking Device</a>. This process makes no logical difference to the code but renders it unintelligible to humans.</p>
<p>The <strong>COBOL Record Definition (CRD)</strong> can be coded to generate or parse any XML tag names, attributes, namespaces, SOAP and mixed content elements. Very large documents are generated/parsed using multiple calls to the interface so that machine storage is kept to a minimum.</p>
<p>Generated XML documents conform to the World Wide Web Consortium (W3C) <a href="https://www.w3.org/TR/xml/" title="Extensible Mark-up Language (XML) 1.0 Definition" class="externalText" target="_blank">Extensible Mark-up Language (XML) 1.0</a> definition.</p>
<p>Parsed XML documents are checked for syntax errors. In the event of an error, a message is returned, identifying the erroneous character position within the input document. No schema or DTD validation is performed.</p>
<p>XML generation rate is <strong>9.9 megabytes per second</strong>; parsing rate is <strong>7.4 megabytes per second</strong>. All benchmark timings are based on an IBM zSeries mainframe running z/OS 1.10. Maximum on-line document size is <strong>99 megabytes</strong>; there is no maximum document size for batch applications.</p>

